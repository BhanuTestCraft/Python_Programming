"""Arithmetic opertaor: These opeartors are used with the numeric values to perform common mathematical operations.
---> Addition(+), Subtraction(-),Multiplication(*),Divison(/),Modulus(%=remainder),Exponent(**),Floor divison(//)
Note: Divison(/) always give the result in float.(eg: 10/10=1.0)
Note: Floor Divison(//) always give the integer value.(We always need to select the lower integer eg: 10//4=2)
eg: 13/7=1.8571. 13//7=1
eg: -10//4=-3
"""

a=10
b=4
print(a+b) #addition operation
print(a-b) #subtraction operation
print(a*b) #multiplication operator
print(a/b) #divison operator
print(a**b)#exponent operator
print(a//b)#Floor divison
print(a%b) #modulus operator
