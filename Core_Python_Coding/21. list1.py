"""
Lists
To store multiple values in the single variable.
"""
bhanu = [81, 87, 45, 67, 90]
print(bhanu, type(bhanu))

sheetal = [83, 57, 75, 61, 99]
print(sheetal, type(sheetal))

# List are similar to arrays but not the same here we can store any data type inside the list as shown below.
y = [1, 2, 3, 56.9, "bhanu", False, True]
print(y, type(y))
