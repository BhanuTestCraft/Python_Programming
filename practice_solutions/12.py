"""
Write a Python program that takes two numbers as input and performs the following operations: addition, subtraction, multiplication, division, and modulus. Display the results.
"""

num1 = int(input("Enter the first number= "))
num2 = int(input("Enter the second number= "))

addition = num1 + num2
print(f"addition of num1 and num2 is {addition}")

subtraction = num1 - num2
print(f"subtraction of num1 and num2 is {subtraction}")

multiplication = num1 * num2
print(f"multiplication of num1 and num2 is {multiplication}")

divison = num1 / num2
print(f"division of num1 and num2 is {divison}")

modulus = num1 % num2
print(f"modulus of num1 and num2 is {modulus}")
