"""
Convert a string to an integer and vice versa.
"""
# converting string to integer.
number_string = "100"
print(number_string, type(number_string))
number = int(number_string)
print(number, type(number))

# converting integer to string.
new_number = 200
print(new_number, type(new_number))
new_number_string = str(new_number)
print(new_number_string, type(new_number_string))
