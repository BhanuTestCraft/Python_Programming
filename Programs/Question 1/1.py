"""
The task is to write a Python program to find the addition of these two numbers. 
"""
# Taking two inputs from the user i.e num1 and num2.
num1 = float(input("Enter the value of first number= "))
num2 = float(input("Enter the value of second number= "))

# adding two numbers.
total = num1 + num2

# print the result in console.
print(f"Sum of the given numbers num1 and num2 is = {total}")
